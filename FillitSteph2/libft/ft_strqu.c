#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;
	int j;

	if (s1 == 0)
		return (0);
	if (s2 == 0)
		return (0);
	i = 0;
	j = 0;
	while (s1[i] == s2[j] && s1[i] != '\0' && s2[j] != '\0')
	{
		i++;
		j++;
	}
	if ((s1[i] - s2[j]) == 0)
		return (1);
	else
		return (0);
}
